    public void setJenisKelamin(String jk) {
        if (jk != null && jk.equalsIgnoreCase("L")) {
            RadioButton_Laki.setSelected(true);
        } else if (jk != null && jk.equalsIgnoreCase("P")) {
            RadioButton_Perempuan.setSelected(true);
        }
    }

